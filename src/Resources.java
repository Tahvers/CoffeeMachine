public class Resources {

    int water, milk, beans, disposableCups, money, cupsMade;
    boolean needCleaning;

    public Resources(int water, int milk, int beans, int disposableCups, int money) {
        this.water = water;
        this.milk = milk;
        this.beans = beans;
        this.disposableCups = disposableCups;
        this.money = money;
        this.cupsMade = 0;
        this.needCleaning = false;

    }
}
